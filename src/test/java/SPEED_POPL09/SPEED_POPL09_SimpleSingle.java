
class SPEED_POPL09_SimpleSingle {
    void SimpleSingle(int n, boolean b) {
        int x = 0;
        int R1 = 0;
        while (x < n) {
            if (b) {
                x = x + 1;
            }
            else {
                x = x + 1;
            }
            R1 = R1 + 1;
        }
    }
}

